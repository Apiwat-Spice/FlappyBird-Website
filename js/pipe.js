/**
 * =====================================
 * Pipe Class
 * =====================================
 * จัดการท่ออุปสรรคในเกม
 *
 * หน้าที่:
 * - สุ่มความสูงของท่อบน
 * - สร้างช่องว่างให้นกบินผ่าน
 * - เคลื่อนที่จากขวาไปซ้าย
 * - วาดท่อบนและท่อล่าง
 */
class Pipe {

    constructor(x) {

        // =====================
        // Position
        // =====================
        // ตำแหน่งเริ่มต้นของท่อ
        // โดยปกติจะเริ่มจากขอบขวาของจอ

        this.x = x;

        // =====================
        // Size
        // =====================
        // width = ความกว้างของท่อ
        // gap   = ช่องว่างระหว่างท่อบนและล่าง

        this.width = 60;
        this.gap = 150;

        // =====================
        // Random Height
        // =====================
        // สุ่มความสูงของท่อบน
        //
        // ช่วงที่เป็นไปได้:
        // 50 - 250

        this.topHeight =
            Math.random() * 200 + 50;

        // =====================
        // Movement
        // =====================
        // ความเร็วในการเลื่อนของท่อ

        this.speed = 3;

        // =====================
        // Score Tracking
        // =====================
        // ใช้ตรวจว่านกผ่านท่อนี้แล้วหรือยัง
        // เพื่อป้องกันการบวกคะแนนซ้ำ

        this.passed = false;
    }

    /**
     * อัปเดตตำแหน่งท่อทุกเฟรม
     *
     * ท่อจะเลื่อนจากขวาไปซ้าย
     */
    update() {

        this.x -= this.speed;
    }

    /**
     * วาดท่อบน Canvas
     *
     * @param {CanvasRenderingContext2D} ctx
     */
    draw(ctx) {

        // =====================
        // Top Pipe
        // =====================
        // ตัวท่อด้านบน

        ctx.fillStyle = "#2ecc71";

        ctx.fillRect(
            this.x,
            0,
            this.width,
            this.topHeight
        );

        // =====================
        // Top Pipe Cap
        // =====================
        // หัวท่อด้านบน

        ctx.fillStyle = "#27ae60";

        ctx.fillRect(
            this.x - 5,
            this.topHeight - 20,
            this.width + 10,
            20
        );

        // =====================
        // Bottom Pipe
        // =====================
        // ตัวท่อด้านล่าง

        ctx.fillStyle = "#2ecc71";

        ctx.fillRect(
            this.x,
            this.topHeight + this.gap,
            this.width,
            600
        );

        // =====================
        // Bottom Pipe Cap
        // =====================
        // หัวท่อด้านล่าง

        ctx.fillStyle = "#27ae60";

        ctx.fillRect(
            this.x - 5,
            this.topHeight + this.gap,
            this.width + 10,
            20
        );
    }
}